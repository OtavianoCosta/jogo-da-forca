import random
def jogar():
    imprimir_abertura()
    palavra_secreta = imprimir_escolhedordepalavra()
    letras_acertadas = inicializa_letras_acertadas(palavra_secreta)

    enforcou = False
    acertou = False
    erros = 0

    print(letras_acertadas)

    while (not enforcou and not acertou):
        chute = pede_chute()
        if(chute in palavra_secreta):
            marcador_chute_correto(palavra_secreta, chute, letras_acertadas)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if (acertou):
        imprime_palavra_vencedor()
    else:
        imprime_palavra_perdedor(palavra_secreta)



def imprimir_abertura():
    print("Boa noite meus queridos")
    print("Bem vindos ou Jogo de Forca do Ota")

def imprimir_escolhedordepalavra():
    arquivo = open("palavras", "r")
    palavrasjogo = []

    for linha in arquivo:
        linha = linha.strip()
        palavrasjogo.append(linha)

    arquivo.close()

    numero = random.randrange(0, len(palavrasjogo))
    palavra_secreta = palavrasjogo[numero].upper()
    return palavra_secreta

def inicializa_letras_acertadas(palavra):
    return ["_" for letra in palavra]

def pede_chute():
     chute = input("Qual a letra?")
     chute = chute.strip().upper()
     return chute
def marcador_chute_correto(palavra_secreta, chute, letras_acertadas):
    index = 0
    for letra in palavra_secreta:
        if (chute == letra):
            letras_acertadas[index] = letra
        index += 1
